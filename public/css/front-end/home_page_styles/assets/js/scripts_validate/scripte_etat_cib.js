function Verifemail(adresse)
{
	
	var place = adresse.indexOf("@",1);
	var point = adresse.indexOf(".",place+1);
	if ((place > -1)&&(adresse.length >2)&&(point > 1))
		{		
		   return(true);
		}
	else
		{
		// alert('Entrez une adresse e-mail valide!!');
		 return(false);
		}
}
